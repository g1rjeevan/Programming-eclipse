package Patterns;

public class Elevenpattern {

	public static void main(String[] args) {
		
		int line=12,n=1,space=line-1;
		for (int i = 0; i <5; i++) {
			for (int j = 0; j <=space+4; j++) {
				System.out.print(" ");
			}
			
				for (int j = 0; j <i; j++) {
					System.out.print("   "+n);
					n=n*(i-j)/(j+1);
				}
				
				System.out.print("  "+n);
				space=space-2;
				System.out.println();
			
		}
	}
	
}
